"""
SMAQ Hybrid Attention Proof Harness
------------------------------------
Validates the theoretical extensions of SMAQ to hybrid attention architectures.

1. Theorem H1 (Sliding Window Eviction):
   For sliding-window layers, keys beyond the window are NEVER read.
   Discarding them saves memory with zero quality impact.

2. Theorem H2 (SSM-SMAQ Metric):
   Spectral shaping using output-projection covariance Σ_C (the SSM analogue
   of query covariance Σ_q) reduces output MSE on synthetic recurrent traces,
   just as SMAQ reduces logit MSE on attention traces.

3. Theorem H3 (SSM Rotation Invariance):
   Orthogonal pre-conditioning of SSM hidden states does not improve adaptive
   quantizer quality — the same wall that holds for attention keys holds for
   SSM states.

4. Theorem H4 (Error Persistence):
   Channels with slowly-decaying state-transition eigenvalues (|λ| ≈ 1)
   amplify quantization error across time steps.  The persistence-weighted
   SSM-SMAQ metric allocates more resolution to these channels.

Usage:
    uv run python hybrid_proof.py
"""

import torch
import math


# ---------------------------------------------------------------------------
# Synthetic SSM Trace Generation
# ---------------------------------------------------------------------------

def generate_synthetic_ssm_trace(
    N: int = 4000,
    state_dim: int = 16,
    condition_ratio: float = 40.0,
    spectral_radius: float = 0.95,
    seed: int = 42,
) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """Generate synthetic SSM hidden states and output projections.

    Mimics a Mamba-style SSM with:
    - Diagonal state transition A with eigenvalues spread between
      spectral_radius and spectral_radius^{condition_ratio}
    - Random orthogonal output projection C with skewed covariance
    - Hidden states generated by running the recurrence

    Returns:
        (H, C, A_eigs, Sigma_C) where:
        - H: (N, state_dim) hidden state trace
        - C: (N, state_dim) output projection vectors
        - A_eigs: (state_dim,) diagonal of discretized A
        - Sigma_C: (state_dim, state_dim) output covariance
    """
    rng = torch.Generator().manual_seed(seed)

    # 1. State transition eigenvalues: mix of fast-decaying and slow-decaying
    A_eigs = torch.linspace(spectral_radius ** condition_ratio, spectral_radius, state_dim)

    # 2. Input matrix B and driving noise
    B = torch.randn((state_dim,), generator=rng)

    # 3. Generate hidden state trace by running the recurrence
    H = torch.zeros((N, state_dim))
    h = torch.zeros(state_dim)
    for t in range(N):
        x_t = torch.randn(1, generator=rng).item()
        h = A_eigs * h + B * x_t
        H[t] = h

    # 4. Generate output projections with skewed covariance
    base_evals = torch.ones(state_dim)
    base_evals[0] = condition_ratio  # Dominant read direction
    A_basis = torch.randn((state_dim, state_dim), generator=rng)
    Q_basis, _ = torch.linalg.qr(A_basis)
    C = torch.randn((N, state_dim), generator=rng)
    C = C * torch.sqrt(base_evals)
    C = C @ Q_basis.T

    # 5. Compute output covariance
    Sigma_C = (C.T @ C) / N

    return H, C, A_eigs, Sigma_C


# ---------------------------------------------------------------------------
# K-means (reused from proof.py)
# ---------------------------------------------------------------------------

def kmeans(data, n_centroids, n_iters=15, seed=42):
    N, D = data.shape
    rng = torch.Generator().manual_seed(seed)
    if N <= n_centroids:
        res = torch.zeros((n_centroids, D), device=data.device, dtype=data.dtype)
        res[:N] = data
        return res

    indices = [torch.randint(N, (1,), generator=rng).item()]
    for _ in range(n_centroids - 1):
        dists = torch.cdist(data, data[indices]).min(dim=1).values
        probs = dists ** 2
        probs = probs / (probs.sum() + 1e-12)
        idx = torch.multinomial(probs, 1, generator=rng).item()
        indices.append(idx)
    centroids = data[indices].clone()
    for _ in range(n_iters):
        dists = torch.cdist(data, centroids)
        assignments = dists.argmin(dim=1)
        new_centroids = torch.zeros_like(centroids)
        counts = torch.zeros(n_centroids)
        new_centroids.index_add_(0, assignments, data)
        counts.index_add_(0, assignments, torch.ones(N))
        mask = counts > 0
        new_centroids[mask] /= counts[mask].unsqueeze(1)
        new_centroids[~mask] = centroids[~mask]
        centroids = new_centroids
    return centroids


def output_mse(C, H, H_hat):
    """Output MSE: E[(C(h - h_hat))^2] — the SSM analogue of logit MSE."""
    delta = H - H_hat
    output_errors = (C * delta).sum(dim=1)
    return (output_errors ** 2).mean().item()


# ---------------------------------------------------------------------------
# Spectral shaping functions
# ---------------------------------------------------------------------------

def ssf_log(eigvals, c=5.0):
    shaped = torch.log1p(c * eigvals.clamp(min=0))
    log_shaped = torch.log(shaped.clamp(min=1e-8))
    log_shaped = log_shaped - log_shaped.mean()
    return torch.exp(log_shaped)


def persistence_weights(A_eigs, max_amp=1000.0):
    """Steady-state error amplification: 1/(1-|λ|²)."""
    abs_sq = A_eigs.abs().square()
    return 1.0 / (1.0 - abs_sq.clamp(max=1.0 - 1.0 / max_amp))


